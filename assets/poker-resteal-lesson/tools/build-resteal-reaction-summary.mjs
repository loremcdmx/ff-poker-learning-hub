import { readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const toolRoot = dirname(fileURLToPath(import.meta.url));
const inputPath = resolve(toolRoot, "../data/field-opener-response.json");
const outputPath = resolve(toolRoot, "../data/resteal-reaction-summary.json");

const openerPositions = ["CO", "BTN"];
const heroPositions = ["SB", "BB"];
const openSizes = [2, 2.5, 3];
const depthBands = ["25-30", "30-35", "35-40"];
const responseActions = ["F", "C", "R"];
const ranks = ["A", "K", "Q", "J", "T", "9", "8", "7", "6", "5", "4", "3", "2"];
const canonicalHands = ranks.flatMap((rowRank, rowIndex) => ranks.map((columnRank, columnIndex) => {
  if (rowIndex === columnIndex) return `${rowRank}${columnRank}`;
  if (rowIndex < columnIndex) return `${rowRank}${columnRank}s`;
  return `${columnRank}${rowRank}o`;
}));
const canonicalHandSet = new Set(canonicalHands);
// JSON object keys that look like integers are serialized first in numeric order.
// Make that language rule explicit so the generated byte order stays intentional.
const canonicalJsonHandOrder = [
  ...canonicalHands.filter((hand) => /^\d\d$/.test(hand)).sort((left, right) => Number(left) - Number(right)),
  ...canonicalHands.filter((hand) => !/^\d\d$/.test(hand))
];

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function splitWindow(window) {
  const parts = String(window || "").split("..");
  assert(parts.length === 2 && parts.every(Boolean), `Invalid source window: ${window}`);
  return parts;
}

function spotKey(openerPosition, heroPosition, openSizeBb, depthBand) {
  return `${openerPosition}:${heroPosition}:${openSizeBb}:${depthBand}`;
}

function emptySpot(openerPosition, heroPosition, openSizeBb, depthBand) {
  return {
    key: spotKey(openerPosition, heroPosition, openSizeBb, depthBand),
    openerPosition,
    heroPosition,
    openSizeBb,
    depthBand,
    totals: { F: 0, C: 0, R: 0, N: 0 },
    hands: {}
  };
}

const source = JSON.parse(readFileSync(inputPath, "utf8"));
assert(source && typeof source === "object", "Strict opener-response source must be an object");
assert(source.meta && source.meta.source && source.meta.actions, "Strict opener-response source is missing metadata");
assert(Array.isArray(source.rows), "Strict opener-response source is missing rows[]");

const [windowStartInclusive, windowEndExclusive] = splitWindow(source.meta.source.window);
const spots = openerPositions.flatMap((openerPosition) => heroPositions.flatMap((heroPosition) => openSizes.flatMap((openSizeBb) => depthBands.map((depthBand) => (
  emptySpot(openerPosition, heroPosition, openSizeBb, depthBand)
)))));
const spotsByKey = new Map(spots.map((spot) => [spot.key, spot]));

for (const [index, row] of source.rows.entries()) {
  const openSizeBb = Number(row.openSizeBb);
  const n = Number(row.n);
  assert(openerPositions.includes(row.openerPosition), `Unexpected openerPosition at row ${index}: ${row.openerPosition}`);
  assert(heroPositions.includes(row.heroPosition), `Unexpected heroPosition at row ${index}: ${row.heroPosition}`);
  assert(openSizes.includes(openSizeBb), `Unexpected openSizeBb at row ${index}: ${row.openSizeBb}`);
  assert(depthBands.includes(row.depthBand), `Unexpected depthBand at row ${index}: ${row.depthBand}`);
  assert(responseActions.includes(row.responseAction), `Unexpected responseAction at row ${index}: ${row.responseAction}`);
  assert(Number.isSafeInteger(n) && n >= 0, `Invalid n at row ${index}: ${row.n}`);
  assert(row.hand === "unknown" || canonicalHandSet.has(row.hand), `Unexpected hand at row ${index}: ${row.hand}`);

  const key = spotKey(row.openerPosition, row.heroPosition, openSizeBb, row.depthBand);
  const spot = spotsByKey.get(key);
  assert(spot, `Missing initialized spot for ${key}`);
  spot.totals[row.responseAction] += n;
  spot.totals.N += n;

  if (!responseActions.slice(1).includes(row.responseAction) || row.hand === "unknown") continue;
  const hand = spot.hands[row.hand] || { C: 0, R: 0, N: 0 };
  hand[row.responseAction] += n;
  hand.N += n;
  spot.hands[row.hand] = hand;
}

for (const spot of spots) {
  spot.hands = Object.fromEntries(canonicalJsonHandOrder.filter((hand) => spot.hands[hand]?.N > 0).map((hand) => [hand, spot.hands[hand]]));
}

const heroJams = Number(source.meta.source.heroJamsTotal);
const matchedResponses = Number(source.meta.source.matchedOpenerResponsesTotal);
const matchRatePct = Number(source.meta.source.matchPct);
const ambiguousJamsExcluded = Number(source.meta.source.ambiguousHeroJamsTotal);
for (const [label, value] of Object.entries({ heroJams, matchedResponses, matchRatePct, ambiguousJamsExcluded })) {
  assert(Number.isFinite(value) && value >= 0, `Invalid source coverage ${label}: ${value}`);
}
assert(spots.reduce((sum, spot) => sum + spot.totals.N, 0) === matchedResponses, "Spot totals do not reconcile with matched opener responses");

const output = {
  meta: {
    // Anchor the generated snapshot to its exclusive source boundary so rebuilding is byte-for-byte deterministic.
    generatedAt: windowEndExclusive,
    sourceVersion: source.meta.version,
    windowStartInclusive,
    windowEndExclusive,
    heroJams,
    matchedResponses,
    matchRatePct,
    ambiguousJamsExcluded,
    actions: {
      F: source.meta.actions.F,
      C: source.meta.actions.C,
      R: source.meta.actions.R
    }
  },
  spots
};

writeFileSync(outputPath, `${JSON.stringify(output)}\n`);
console.log(JSON.stringify({
  inputPath,
  outputPath,
  inputBytes: statSync(inputPath).size,
  outputBytes: statSync(outputPath).size,
  spots: spots.length,
  matchedResponses,
  knownContinuingHands: spots.reduce((sum, spot) => sum + Object.values(spot.hands).reduce((handSum, hand) => handSum + hand.N, 0), 0)
}, null, 2));
